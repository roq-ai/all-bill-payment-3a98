import { type CheckSelect, fetcher, marshal, makeUrl } from '@roq/client';
import { Fetcher, SWRConfiguration } from 'swr';
import type { Prisma, merchant, user } from './types';
import { DefaultArgs, GetFindResult } from './types/runtime/library';

export type RequestOptions = Record<string, any>;
export type SWRRequestOptions<Result, Error = any> = {
  disabled?: boolean;
  initialData?: Result;
} & SWRConfiguration<Result, Error, Fetcher<Result>>;

export const defaultEndpoint = process.env.NEXT_PUBLIC_API_URL || process.env.API_URL;

interface MerchantSDK {
  create: <T extends Prisma.merchantCreateArgs, R extends CheckSelect<T, merchant, Prisma.merchantGetPayload<T>>>(
    data: Prisma.SelectSubset<T, Prisma.merchantCreateArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  createMany: <T extends Prisma.merchantCreateManyArgs, R extends Prisma.BatchPayload>(
    args: Prisma.SelectSubset<T, Prisma.merchantCreateManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  update: <T extends Prisma.merchantUpdateArgs, R extends Prisma.merchantGetPayload<T>>(
    data: Prisma.SelectSubset<T, Prisma.merchantUpdateArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  updateMany: <T extends Prisma.merchantUpdateManyArgs, R extends Prisma.BatchPayload>(
    args: Prisma.SelectSubset<T, Prisma.merchantUpdateManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  upsert: <T extends Prisma.merchantUpsertArgs, R extends Prisma.merchantGetPayload<T>>(
    data: Prisma.SelectSubset<T, Prisma.merchantUpsertArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  delete: <T extends Prisma.merchantDeleteArgs, R extends Prisma.merchantGetPayload<T>>(
    data: Prisma.SelectSubset<T, Prisma.merchantDeleteArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  deleteMany: <T extends Prisma.merchantDeleteManyArgs, R extends Prisma.BatchPayload>(
    args: Prisma.SelectSubset<T, Prisma.merchantDeleteManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  findMany: <T extends Prisma.merchantFindManyArgs, R extends GetFindResult<Prisma.$merchantPayload<DefaultArgs>, T>[]>(
    args: Prisma.SelectSubset<T, Prisma.merchantFindManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  count: <T extends Prisma.merchantCountArgs, R extends number>(
    args: Prisma.SelectSubset<T, Prisma.merchantCountArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  findManyWithCount: <
    T extends Prisma.merchantFindManyArgs,
    R extends GetFindResult<Prisma.$merchantPayload<DefaultArgs>, T>[],
    C extends number,
  >(
    args: Prisma.SelectSubset<T, Prisma.merchantFindManyArgs>,
    options?: RequestOptions,
  ) => Promise<{ data: R; count: C }>;

  findFirst: <T extends Prisma.merchantFindFirstArgs, R extends GetFindResult<Prisma.$merchantPayload<DefaultArgs>, T>>(
    args?: Prisma.SelectSubset<T, Prisma.merchantFindFirstArgs>,
    options?: RequestOptions,
  ) => Promise<R>;
}

